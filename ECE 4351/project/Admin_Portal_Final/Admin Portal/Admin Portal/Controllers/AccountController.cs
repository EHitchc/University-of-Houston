        [Authorize(Roles = "Admin,Manager")]
        public async Task<ActionResult> Links(string id)
        {

            var model = new LinksViewModel();
            model.User = await _userManager.FindByIdAsync(id);

            var assignedRoles = await _userManager.GetRolesAsync(model.User);
            var tempRoles = _role.Role.ToList();
            var setRoles = new List<Role>();
            var availableRoles = new List<Role>();

            foreach (var role in tempRoles)
            {
                if(assignedRoles.Contains(role.Description) || role.IsGlobal)
                {
                    setRoles.Add(role);
                }
                else
                {
                    availableRoles.Add(role);
                }
            }
            model.Roles = setRoles.OrderBy(r => r.Description);
            model.AvailableRoles = availableRoles.OrderBy(r => r.Description);

            return View(model);
        }

