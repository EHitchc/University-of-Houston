        [HttpPost]
        [Authorize(Roles = "Admin")]
        [ValidateAntiForgeryToken]
        public async Task<IActionResult> Register(RegisterViewModel model, string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                var user = new ApplicationUser { UserName = model.Email, Email = model.Email, EName = model.EName};
                
                var result = await _userManager.CreateAsync(user, model.Password);
                if (result.Succeeded)
                {
                    _logger.LogInformation("User created a new account with password.");

                    var code = await _userManager.GenerateEmailConfirmationTokenAsync(user);
                    var callbackUrl = Url.EmailConfirmationLink(user.Id, code, Request.Scheme);
                    await _emailSender.SendEmailConfirmationAsync(model.Email, callbackUrl);

                    //await _signInManager.SignInAsync(user, isPersistent: false);
                    //_logger.LogInformation("User created a new account with password.");
                    if(string.Equals(model.BasePrivilegeLevel, "Admin"))
                    {
                        await _userManager.AddToRoleAsync(user, "Manager"); // Include Manager Rights as well as Admin
                    }
                    await _userManager.AddToRoleAsync(user, model.BasePrivilegeLevel); // Selected Privilege Level
                    if (!string.Equals(model.BasePrivilegeLevel, "Employee")) // Avoid Redundancy
                    {
                        await _userManager.AddToRoleAsync(user, "Employee"); // All users counted as Employee by default
                    }

                    return RedirectToLocal(returnUrl);
                }
                AddErrors(result);
            }

            // If we got this far, something failed, redisplay form
            return View(model);
        }

