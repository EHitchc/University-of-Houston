        [HttpPost]
        [ValidateAntiForgeryToken]
        [Authorize(Roles = "Admin,Manager")]
        public async Task<IActionResult> Edit(EditUserViewModel model, string returnUrl = null)
        {
            ViewData["ReturnUrl"] = returnUrl;
            if (ModelState.IsValid)
            {
                // Get the existing student from the db
                var user = await _userManager.FindByIdAsync(model.Id);

                // Update it with the values from the view model
                if(!string.IsNullOrEmpty(model.EName))
                    user.EName = model.EName;
                if (!string.IsNullOrEmpty(model.Email))
                {
                    user.UserName = model.Email;
                    user.Email = model.Email;
                }
                if (!string.IsNullOrEmpty(model.Password))
                    await _userManager.ResetPasswordAsync(user, await _userManager.GeneratePasswordResetTokenAsync(user), model.Password);

                if (!string.IsNullOrEmpty(model.BasePrivilegeLevel))
                {
                    string [] roles = { "ADMIN", "EMPLOYEE", "MANAGER" };
                    await _userManager.RemoveFromRoleAsync(user, roles[0]);
                    await _userManager.RemoveFromRoleAsync(user, roles[1]);
                    await _userManager.RemoveFromRoleAsync(user, roles[2]);

                    if (string.Equals(model.BasePrivilegeLevel, "Admin"))
                    {
                        await _userManager.AddToRoleAsync(user, "Manager"); // Include Manager Rights as well as Admin
                    }
                    await _userManager.AddToRoleAsync(user, model.BasePrivilegeLevel); // Selected Privilege Level
                    if (!string.Equals(model.BasePrivilegeLevel, "Employee")) // Avoid Redundancy
                    {
                        await _userManager.AddToRoleAsync(user, "Employee"); // All users counted as Employee by default
                    }
                }

                // Apply the changes if any to the db
                await _userManager.UpdateAsync(user);
            }

            return RedirectToLocal(returnUrl);
        }

